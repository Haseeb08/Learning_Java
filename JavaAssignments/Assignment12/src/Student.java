public class Student {
        int id;
        String name;
        int age;
        String gender;
        int enrollmentYear;
        float perTillDate;
        String engDepartment;


    public Student(int id, String name, int age, String gender, String engDepartment, int enrollmentYear,
        float perTillDate) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.engDepartment = engDepartment;
        this.enrollmentYear = enrollmentYear;
        this.perTillDate = perTillDate;
    }
}
